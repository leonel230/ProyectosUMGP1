    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/nominabancos", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from bancobantrab where ID = ?");
/*-----------------------------------------------------------------------------------------------------------------------*/

            //pst.setString(1, txt_buscar.getText().trim());//busca lo que hay en el txt en la base de datos

            ResultSet rs = pst.executeQuery();

            if(rs.next()){
                //Busca lo que está en las " " y lo trae a los txt correspondientes
                cod_carrera.setText(rs.getString("codigo_carrera"));
                cod_sede.setText(rs.getString("codigo_sede"));
                cod_jornada.setText(rs.getString("codigo_jornada"));
                cod_seccion.setText(rs.getString("codigo_seccion"));
                cod_aula.setText(rs.getString("codigo_aula"));
                cod_curso.setText(rs.getString("codigo_curso"));
                car_alumno.setText(rs.getString("carnet_alumno"));
                nota_asig.setText(rs.getString("nota_asignacioncursoalumnos"));
            } else {
                //si el campo no existiera que aparecera un mensaje en el LABEL
                JOptionPane.showMessageDialog(null, "Alumno no registrado.");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_jButton2ActionPerformed


